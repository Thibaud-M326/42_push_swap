#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*mem_s;

	i = 0;
	mem_s = (char *)s;
	while (i < n)
	{
		mem_s[i] = c;
		i++;
	}
	return (s);
}
